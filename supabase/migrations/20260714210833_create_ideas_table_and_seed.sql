/*
# Create ideas table with full business properties + seed 20 ideas

Columns:
- id (int, PK)
- name (text)
- category (text)
- capital (int) — startup budget in rubles
- complexity (text) — Низкая / Средняя / Высокая
- potential (text) — Средний / Высокий
- rating (numeric) — 0-5 scale
- risk_level (text) — Низкий / Средний / Высокий
- upvotes (int) — total upvote count (denormalized for filtering)
- description (text)
- why (text)
- pros (text)
- cons (text)
- how_to_start (text)
- risks (text)
- created_at (timestamptz)
*/

CREATE TABLE IF NOT EXISTS ideas (
  id int PRIMARY KEY,
  name text NOT NULL,
  category text NOT NULL,
  capital int NOT NULL,
  complexity text NOT NULL,
  potential text NOT NULL,
  rating numeric(2,1) NOT NULL DEFAULT 0,
  risk_level text NOT NULL DEFAULT 'Средний',
  upvotes int NOT NULL DEFAULT 0,
  description text NOT NULL,
  why text NOT NULL,
  pros text NOT NULL,
  cons text NOT NULL,
  how_to_start text NOT NULL,
  risks text NOT NULL,
  created_at timestamptz DEFAULT now()
);

ALTER TABLE ideas ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "ideas_select_all" ON ideas;
CREATE POLICY "ideas_select_all" ON ideas FOR SELECT
  TO anon, authenticated USING (true);

DROP POLICY IF EXISTS "ideas_insert_authenticated" ON ideas;
CREATE POLICY "ideas_insert_authenticated" ON ideas FOR INSERT
  TO authenticated WITH CHECK (true);

DROP POLICY IF EXISTS "ideas_update_authenticated" ON ideas;
CREATE POLICY "ideas_update_authenticated" ON ideas FOR UPDATE
  TO authenticated USING (true) WITH CHECK (true);

CREATE INDEX IF NOT EXISTS idx_ideas_category ON ideas(category);
CREATE INDEX IF NOT EXISTS idx_ideas_capital ON ideas(capital);
CREATE INDEX IF NOT EXISTS idx_ideas_rating ON ideas(rating);
CREATE INDEX IF NOT EXISTS idx_ideas_complexity ON ideas(complexity);
CREATE INDEX IF NOT EXISTS idx_ideas_risk_level ON ideas(risk_level);
CREATE INDEX IF NOT EXISTS idx_ideas_upvotes ON ideas(upvotes);

